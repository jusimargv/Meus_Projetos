# Bloco em Python
def sacar( valor): # início do bloco do método 
    saldo = 500
    
    if saldo >= valor: # início do bloco do if
        
        
        print("Saque Realizado com Sucesso!")
    elif saldo <= valor:
        print("Saldo Insuficiente!")
        
        #fim do bloco do if
sacar(100) # fim do bloco do método       
            